			<p>In dit hoofdstuk ga je leren wat een UI is en wat UX is en hoe je deze goed kunt maken. UI staat voor user interface, dit is hoe je het apparaat of een programma bediend. UX staat voor user experience, dit is hoe fijn iemand het vind om iets te gebruiken. In dit hoofdstuk zullen we ons beperken tot software. Deze website heeft ook een UI, het is eigenlijk alles dat zichtbaar is voor de gebruiker. Je zou de UI zelfs in onderdelen kunnen opdelen: een navigatiebalk, loginveld, theorieblok, enz. Bij UX gaat het meer om de werking van het geheel, met vragen zoals: 'Kun je makkelijk komen waar je heen wilt?', 'Is de software wel nuttig?', 'Is het makkelijk te gebruiken?'. UI en UX kunnen soms overlappen en ondersteunen elkaar. Over het algemeen zullen details onder UI vallen en de samenhang onder UX.</p>

			<p>Wat is een goede UI?</p>

			<p>Een goede UI voldoet aan een aantal eisen:</p>

			<p>Het is intuïtief - de software werkt zoals je zou verwachten</p>

			<p>Het is consistent - pagina's hebben een voorspelbare en soortgelijke structuur</p>

			<p>Het is responsief - het reageert snel op input en laat de gebruiker zo min mogelijk wachten</p>

			<p>Het is flexibel - het moet later makkelijk aan te passen zijn</p>

			<p>Het is mooi - zaken zoals: de kleuren, het lettertype en de lay-out zien er goed uit</p>

			<p>Wat is een goede UX?</p>

			<p>Een goede UX voldoet aan een paar eisen:</p>

			<p>Het is intuïtief - de software werkt zoals je zou verwachten</p>

			<p>Het is consistent - de software heeft een duidelijke structuur</p>

			<p>Het is nuttig - de software lost een probleem op</p>

			<p>Het is bruikbaar - de software is makkelijk in gebruik</p>

			<p>Het is toegankelijk - de software is goed te begrijpen en kan gemakkelijk worden opgepakt</p>

