			<p>Op het internet geef je veel informatie af, vrijwillig en onvrijwillig. In dit hoofdstuk ga je leren wat er wordt bijgehouden, hoe en waarom. Je zult ook leren hoe je voor jouw privacy kan opkomen en wie dat voor jou doen.</p>

			<p>Wat wordt bijgehouden?</p>

			<p>Op het internet wordt heel veel informatie over jouw opgeslagen, het hangt erg af van de websites die bezoekt wat er precies bijgehouden wordt. Een voorbeeld is jouw Inforca account, daarop wordt het volgende bijgehouden (soms als ‘lege’ waarde): ID, naam, gebruikersnaam, school, functie, klas, account aanmaakdatum, account verloopdatum, groepsnaam, groepsrol, email, mislukte inlogpogingen, quiz resultaten en theorie progressie. Deze gegevens zijn allemaal aan jouw account gekoppeld aan staan op een database.</p>

			<p>Andere sites houden bijvoorbeeld bij: wat je leuk vind, welke apparaat je gebruikt, waar je bent, hoe oud je bent en hoelang je online actief bent. Er is nog veel meer te bedenken en het kan soms best eng zijn wat allemaal bijgehouden wordt. Je weet vaak niet wie het bijhoudt, hoe en waarom.</p>

			<p>Waarom wordt dit bijgehouden?</p>

			<p>De meeste informatie wordt simpelweg bijgehouden om te zien wie je bent, denk aan jouw naam en gebruikersnaam. Inforca houdt de gegevens bijvoorbeeld bij om: in te loggen, de geldigheid van het account te controleren, jouw account te beveiligen, te kijken hoever je bent en een overzicht te geven aan docenten.</p>

			<p>Veel websites houden ook informatie bij om reclame te tonen. Ze willen zo relevant mogelijke reclame, want daar klik je eerder op en dus krijgen hun meer betaald. Er zijn ook veel sites die bijhouden waar iemand vandaan komt en welk apparaat ze gebruiken. Die informatie is voor een developer handig om te zorgen dat de website zo goed mogelijk op jouw apparaat werkt en in jouw taal beschikbaar is. Het kan natuurlijk ook voor marketing gebruikt worden. Misschien wil je wel een nieuwe telefoon of een bepaalde aanbieding gebruiken.</p>

			<p>Een recente ontwikkeling is dat de data ook gebruik wordt als trainingsdata voor neural networks, een soort AI. Deze AI kan dan leren van de data die afkomstig is van gebruikers van een bepaalde service. Een bekend voorbeeld hiervan zijn de captcha’s van google, dit zijn de uitdagingen om te bewijzen dat je geen bot bent. Een AI wordt getraind door het aangeven in welke afbeelding(en) iets te vinden is.</p>

