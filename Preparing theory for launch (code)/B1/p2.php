			<ol>
				<li>Antwoord vraag 1:

Positief: Binnen educatie is personalisatie belangrijk, kinderen appreciëren het wanneer een leraar ze beter kent, een programma dat intelligentie kan berekenen kan er voor zorgen dat kinderen betere educatie krijgt en niet constant van niveaus veranderen.

Negatief: Hoewel een programma dat in staat is om intelligentie op massale schaal kan berekenen niet direct een van de ACM richtlijnen overschrijdt betekent niet dat het ethisch goed is. Gebruik logica om te voorspellen wat een programma als dit kan doen. Wanneer mensen in staat zijn om anderen te onderscheiden gebaseerd op een zichtbaar verschil zullen extremere groepen mensen kunnen categoriseren. Mensen kunnen privileges en banen krijgen gebaseerd op IQ.

Gevolgenethiek van Bentham en Mill: We baseren ethiek op het resultaat, wanneer een actie meer positieve dan negatieve gevolgen heeft dat is het ethisch goed. Een positief gevolg is gebaseerd op hoeveel goed het brengt in de wereld. Een actie zoals dit zal meer kans hebben om misbruikt te worden en is ethisch niet goed.

Plichtethiek van Kant: Kant geloofde in het categorische imperatief. Dit betekent dat wanneer een actie door iedereen in een samenleving wordt uitgevoerd en de maatschappij blijft neutraal of wordt hier beter op, dan is de actie ethisch goed. Bijvoorbeeld: Als iedereen in een maatschappij zou liegen, dan zal de maatschappij niet beter ervan worden, het zal juist uit elkaar vallen. Daarom is liegen nooit ethisch goed in de ogen van Kant. Een programma om intelligentie te berekenen heeft een grote risico op de samenleving en is dus ethisch niet goed.

Nietzsche: Friedrich Nietzsche geloofde in het nihilisme. Nihilisme is wanneer je denkt dat mensen zo onbelangrijk zijn in het universum dat het leven al zijn betekenis heeft verloren. Nietzsche geloofde ook dat de mens moet proberen een betekenis en een doel voor zichzelf moet creëren, de mens moet proberen een doel in het leven te vinden en zo een hogere mens worden. De hogere mens kan dan de normale mens achterlaten en verder evolueren. Een programma dat intelligentie kan berekenen kan dit process versnellen en de mensheid “verbeteren”, daarom is dit ethisch wel een goed doel om te hebben.

 Antwoord vraag 2:

Positief: Geheimen kunnen gevaarlijk zijn voor een samenleving. Wanneer je het vrije wil van een terrorist overschrijft kan je verdere aanvallen voorkomen. 

Negatief: We leven in een liberale samenleving waarin menselijke vrijheid centraal staat. Om vrijheid te overschrijven is gelijk aan fundamentele rechten van de mens te verbreken. 

Gevolgenethiek: Hoewel er goede gevolgen kunnen komen van een machine zoals dit, zijn de negatieve gevolgen veel te extreem. Dit is een ethisch slechte actie.

Plichtsethiek: Als in een samenleving de vrije wil van iedereen kan worden overschreven zal dit resulteren in een samenleving waarin iedereen veilig is in de ruil voor vrijheid. Als je veiligheid over vrijheid prioriteert zal dit een goede actie zijn, als je vrijheid ziet als een belangrijke recht voor de mens dan is dit een slechte actie.</li>
