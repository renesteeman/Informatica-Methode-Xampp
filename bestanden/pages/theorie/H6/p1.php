			<p>
				Project management is het in goede banen leiden van een project. De definitie van een project is een activiteit om iets te creëren met beperkte middelen. Deze middelen zijn bijvoorbeeld: geld, tijd en informatie.
			</p>
			<p>
				Een project kan in verschillende fasen worden ingedeeld: de ideefase, definitiefase, ontwikkelfase en nazorgfase. Deze fasen maken duidelijk hoever je bent en maken het gemakkelijker om te plannen. De meeste tijd zul je besteden aan de ontwikkelfase, maar die zal alleen succesvol zijn als de idee- en definitiefase goed zijn uitgevoerd.
			</p>
			<p>
				De ideefase gaat over het bedenken wat je als project wilt hebben. Op het eind van deze fase moet het duidelijk zijn wat het project (grofweg) inhoud, dit kan worden gedaan met het SMART-model. SMART staat voor: specifiek, meetbaar, acceptabel, realistisch en tijdgebonden. Met dit model zul je dus aangeven: wat je wilt bereiken, hoe je weet dat je het doel hebt behaald, waarom je dit doel hebt, of het doel haalbaar is (en hoe) en wanneer het project af moet zijn. Het is ook aan te bevelen om een lijst van eisen op te stellen, dan weet je wat je nog moet doen en wanneer jouw product af is. Als je een opdrachtgever hebt moet je deze lijst samen opstellen. <!-- newly added TODO check -->Controleer het idee ook op maatschappelijke aspecten, denk dus na over de invloed van jouw project op de wereld. Het is natuurlijk niet wenselijk dat een project grote negatieve gevolgen heeft. Een voorbeeld hiervan kan het ontwerpen van lootboxes zijn, dit zijn systemen in spellen waarmee een speler een kans krijgt iets te winnen, vaak tegen betaling. Het zou niet wenselijk zijn voor de maatschappij als dit tot verslaafden zou leiden die hierdoor in armoede terechtkomen.
			</p>
			<p>
				In de definitiefase gaat jij je voorbereiden op het uitvoeren van het echte project. Dit doe je door de details van het project uit te werken. Je maakt dus duidelijk: wat het probleem is, waarom je dit wilt oplossen, de effecten van het project en wat de randvoorwaarden zijn. Deze randvoorwaarden kunnen bijvoorbeeld het budget, de opleverdatum, het aantal werkuren en de communicatiewijze zijn. Het is ook een goed idee om een planning te maken, zodat je weet wat wanneer klaar moet zijn en of het project haalbaar blijft of misschien wel te eenvoudig is.
			</p>
			<p>
				De ontwikkelfase is het daadwerkelijk uitvoeren van het gemaakte plan. Deze fase kan vaak worden opgedeeld in kleinere stukken, als je een website wilt maken kan dit bijvoorbeeld: een schets maken, de tekst schrijven, de HTML schrijven en de CSS schrijven zijn. Deze fase is afgelopen als de doelen zijn bereikt. Zorg er ook voor dat het product goed getest wordt en de fouten er zo veel mogelijk uit zijn. Vraag ook voor feedback, het liefst ook van mensen die niet aan het project hebben meegewerkt naast een eventuele opdrachtgever. <!-- newly added TODO check -->Het is erg belangrijk dat er bij (publieke) applicaties ook aandacht wordt besteed aan security. Als je data opslaat moet dit niet beschikbaar zijn voor mensen die hier niet bevoegd voor zijn, zoals hackers. Het gebruiken van een beveiligde verbinding en encryptie kan ook helpen, aangezien ze ervoor zorgen dat de data niet direct leesbaar is.
			</p>
			<p>
				De nazorgfase is de afronding van het project. In deze fase breng je het resultaat van het project uit, misschien gaat de website online of laat je jouw robot zien. Het kan ook zo zijn dat je nog kleine veranderingen aan het resultaat wilt maken, misschien wat fouten er uithalen of een paar dingen toevoegen. Deze fase bevat ook een evaluatie, hierbij kijk je naar wat goed en fout ging en wat je in de toekomst kunt veranderen.
			</p>
			<!-- NEWLY ADDED, TODO check -->
			<p>
				Bij ICT-projecten kun je meerdere soorten onderdelen herkennen. Het ontleden van een 'product' in deze soorten onderdelen heet  decompositie. De soorten onderdelen die je moet kunnen herkennen zijn: fysiek, logisch en toepassingen. Fysiek betekent simpelweg tastbaar, je kunt het aanraken. Een voorbeeld hiervan zijn kabels. Logisch verwijst naar de berederenering achter de keuzes. Toepassingen verwijst naar software.
