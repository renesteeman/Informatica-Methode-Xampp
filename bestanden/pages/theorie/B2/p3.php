			<p>Hoe kun je jouw online privacy verbeteren?</p>

			<p>Het makkelijkste is om informatie die niet nodig is ook niet te geven en op te letten aan wie je informatie geeft. Als je niet wilt dat iedereen je kan vinden, gebruik dan geen sociale media of stel ze goed in. Je kunt ook een search engine kiezen die privacy respecteert. Hetzelfde geldt voor een browser. Zo zijn er veel keuzes die je kunt maken om jouw privacy flink te verbeteren.</p>

			<p>Het is helaas wel vaak zo dat je gemak moet inleveren voor privacy, hoe je dit wilt balanceren is natuurlijk erg persoonlijk. Misschien vind je privacy niet belangrijk en let je er niet op of je wilt juist veel privacy en bent bereid er iets voor in te leveren.</p>

			<p>Je kunt ook jouw verbinding beter beveiligen en privé maken met encryptie. Jouw internetgebruik wordt namelijk door jouw ISP, internet service provider, gemonitord. Het kan zelfs zo zijn dat jouw data ‘afgeluisterd’ wordt. Je kunt lastig weten wat er met jouw data gebeurt als een derde partij er toegang tot heeft. Als je privacy erg belangrij vindt kun je bijvoorbeeld een VPN, virtual private network gebruiken. Een goede VPN houdt jouw data privé en beschermt het ook. Deze VPN’s kosten helaas wel vaak geld.</p>

			<p>Als je je afvraagt welke informatie over jou opgeslagen wordt door een bedrijf of instantie dat in de EU opereert kun je gebruikmaken van GDPR. GDPR is de nieuwe Europese wet databescherming. Het zorgt voor meer transparantie over wat er met jouw data gebeurt en het geeft jou de mogelijk op te vragen wat over jou opgeslagen is. Je kunt dus jouw data altijd opvragen, dit kan via e-mail of een systeem dat hiervoor gebouwd is. Je kunt zelfs jouw data laten verwijderen.</p>

			<p>5 korte tips voor meer privacy zijn:</p>
			<ol>
				<li>Gebruik firefox als browser</li>
				<li>Gebruik een VPN</li>
				<li>Gebruik ecosia of duckduckgo als zoekmachine</li>
				<li>Geef geen onnodige informatie af</li>
				<li>Let op met sociale media</li>
			</ol>

