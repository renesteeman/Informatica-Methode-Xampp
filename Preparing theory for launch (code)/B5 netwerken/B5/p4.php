			<p>Je hebt minder controle</p>

			<p>De cloud kan erg handig zijn als je een programma of opslag wilt delen met meerdere mensen of wilt zorgen dat clients weinig rekenkracht nodig hebben door berekeningen op de cloud te laten plaatsvinden. Stel je wilt iets doen dat veel rekenkracht vereist, maar je hebt geen krachtig apparaat. De cloud kan dan jouw berekening uitvoeren en jou het antwoord sturen, zonder dat je een krachtiger apparaat hoeft te kopen. Je zou ook een website op de cloud kunnen zetten, je hoeft je dan geen zorgen te maken over de bereikbaarheid. De cloud regelt voor jou de rekenkracht en gebruikt zoveel rekenkracht als op dat moment nodig is. Je hoeft dan niet te investeren in krachtige servers terwijl je misschien maar eens in de week die rekenkracht nodig hebt.</p>

