			<p>Cloud</p>

			<p>De cloud is een groep apparaten dat met elkaar verbonden is door een netwerk en rekenkracht en opslag deelt. Je maakt waarschijnlijk al gebruik van de cloud met zaken zoals: e-mail, office 365, google drive en een digitale assistent.</p>

			<p>Voordelen van de cloud zijn:</p>

			<p>De hoeveelheid rekenkracht die je beschikbaar hebt kan steeds worden aangepast aan jouw wensen, je hebt dus (zo goed als) altijd genoeg capaciteit.</p>

